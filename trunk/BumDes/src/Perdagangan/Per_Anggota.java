    private void tabelAnggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelAnggotaMouseClicked
        int row1 = tabelAnggota.getSelectedRow();
        isian_noAnggota.setText(tabelAnggota.getValueAt(row1, 0).toString());//0 kolom pertama
        isian_namaAnggota.setText(tabelAnggota.getValueAt(row1, 1).toString());
        isian_alamat.setText(tabelAnggota.getValueAt(row1, 2).toString());
        tombolRubah.setEnabled(true);
        tombolHapus.setEnabled(true);
        isian_noAnggota.setEditable(false);
    }//GEN-LAST:event_tabelAnggotaMouseClicked

    private void tombolRubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolRubahActionPerformed
        try {
            Anggota agt = new Anggota();
            agt.noAnggota = isian_noAnggota.getText();
            agt.setNamaAnggota(isian_namaAnggota.getText());
            agt.setAlamat(isian_alamat.getText());
            agt.statusTerakhir = "TDK ADA TRANSAKSI";
            Perdagangan.Kontrol.Anggota_Kontrol.getKoneksi().updateAnggota(agt);
            JOptionPane.showMessageDialog(rootPane, "Pengubahan Member Sukses");
            updateTabel();
            bersihkan();
        } catch (SQLException ex) {
            Logger.getLogger(Per_Anggota.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(Per_Anggota.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tombolRubahActionPerformed

