    private void tombol_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombol_loginActionPerformed
        // TODO add your handling code here:
        try {
            if (LoginKontrol.getKoneksi().login(userText.getText(), paswText.getText())) {
                JOptionPane.showMessageDialog(this, "Selamat Datang");
                Beranda asd = new Beranda();
                asd.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Username atau password anda salah");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tombol_loginActionPerformed

