            text_ketiga.setText("0");
            double total = Double.parseDouble(text_abodemen.getText()) + Double.parseDouble(text_pertama.getText())
                    + Double.parseDouble(text_kedua.getText()) + Double.parseDouble(text_ketiga.getText());
