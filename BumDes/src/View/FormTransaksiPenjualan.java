            double sisa = selisih;
            label_pertama.setText(Double.toString(sisa).split("\\.")[0] + " Pertama (" + pertama + "/m3) " + ": ");
            text_pertama.setText(Double.toString(sisa * pertama).split("\\.")[0]);
