package Perdagangan.Kontrol;

/**
 *
 * @author Adhi
 */
public class Barang {

    public String kodeBarang;
    public String namaBarang;
    public int jumlahStok;
    public String satuanBarang;
    public long hargaSatuan;
    public long hrgBarang;

    /**
     * @return the kodeBarang
     */
    public String getKodeBarang() {
        return kodeBarang;
    }

    /**
     * @param kodeBarang the kodeBarang to set
     */
    public void setKodeBarang(String kodeBarang) {
        this.kodeBarang = kodeBarang;
    }

    /**
     * @return the namaBarang
     */
    public String getNamaBarang() {
        return namaBarang;
    }

    /**
     * @param namaBarang the namaBarang to set
     */
    public void setNamaBarang(String namaBarang) {
        this.namaBarang = namaBarang;
    }

    /**
     * @return the jumlahStok
     */
    public int getJumlahStok() {
        return jumlahStok;
    }

    /**
     * @param jumlahStok the jumlahStok to set
     */
    public void setJumlahStok(int jumlahStok) {
        this.jumlahStok = jumlahStok;
    }

    /**
     * @return the satuanBarang
     */
    public String getSatuanBarang() {
        return satuanBarang;
    }

    /**
     * @param satuanBarang the satuanBarang to set
     */
    public void setSatuanBarang(String satuanBarang) {
        this.satuanBarang = satuanBarang;
    }

    /**
     * @return the hargaSatuan
     */
    public long getHargaSatuan() {
        return hargaSatuan;
    }

    /**
     * @param hargaSatuan the hargaSatuan to set
     */
    public void setHargaSatuan(long hargaSatuan) {
        this.hargaSatuan = hargaSatuan;
    }

    /**
     * @return the hrgBarang
     */
    public long getHrgBarang() {
        return hrgBarang;
    }

    /**
     * @param hrgBarang the hrgBarang to set
     */
    public void setHrgBarang(long hrgBarang) {
        this.hrgBarang = hrgBarang;
    }
}
