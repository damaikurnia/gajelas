public class Profil {
    String namadesa;
    String alamatdesa;
    String notelp;
    String desa;
    String kecamatan;
    String kabupaten;
    String provinsi;
    String email;
    String logo;

    /**
     * @return the namadesa
     */
    public String getNamadesa() {
        return namadesa;
    }

    /**
     * @param namadesa the namadesa to set
     */
    public void setNamadesa(String namadesa) {
        this.namadesa = namadesa;
    }

    /**
     * @return the alamatdesa
     */
    public String getAlamatdesa() {
        return alamatdesa;
    }

    /**
     * @param alamatdesa the alamatdesa to set
     */
    public void setAlamatdesa(String alamatdesa) {
        this.alamatdesa = alamatdesa;
    }

    /**
     * @return the notelp
     */
    public String getNotelp() {
        return notelp;
    }

    /**
     * @param notelp the notelp to set
     */
    public void setNotelp(String notelp) {
        this.notelp = notelp;
    }

    /**
     * @return the desa
     */
    public String getDesa() {
        return desa;
    }

    /**
     * @param desa the desa to set
     */
    public void setDesa(String desa) {
        this.desa = desa;
    }

    /**
     * @return the kecamatan
     */
    public String getKecamatan() {
        return kecamatan;
    }

    /**
     * @param kecamatan the kecamatan to set
     */
    public void setKecamatan(String kecamatan) {
        this.kecamatan = kecamatan;
    }

    /**
     * @return the kabupaten
     */
    public String getKabupaten() {
        return kabupaten;
    }

    /**
     * @param kabupaten the kabupaten to set
     */
    public void setKabupaten(String kabupaten) {
        this.kabupaten = kabupaten;
    }

    /**
     * @return the provinsi
     */
    public String getProvinsi() {
        return provinsi;
    }

    /**
     * @param provinsi the provinsi to set
     */
    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the logo
     */
    public String getLogo() {
        return logo;
    }

    /**
     * @param logo the logo to set
     */
    public void setLogo(String logo) {
        this.logo = logo;
    }
}
