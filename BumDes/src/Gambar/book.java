package Gambar;

/**
 *
 * @author Laurens_Widi
 */
public class book {
    private Integer id;
    private String title;
    private String image;

    public book() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    } 
}
